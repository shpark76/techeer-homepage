// Language switcher
let currentLang = localStorage.getItem('techeer-lang') || 'ko';

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('techeer-lang', lang);
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const t = translations[lang]?.[key];
    if (t !== undefined) el.innerHTML = t;
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const t = translations[lang]?.[key];
    if (t !== undefined) el.setAttribute('placeholder', t);
  });

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLanguage(btn.getAttribute('data-lang')));
  });

  if (currentLang !== 'ko') setLanguage(currentLang);
});
